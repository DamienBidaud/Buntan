<h3>Bienvenue sur le site de l'application Buntan</h3>

<p>Buntan, qui signifie 'partage' en japonais, est un logiciel de gestion pour fichiers multimedias.</p>
<p>
Le site de l'application est utilisé pour gérer les comptes d'utilisateur. Il permet de modifier ou de supprimer les informations des utilisateurs de l'application. 
Il dispose aussi d'autres fonctionnalités comme une lecteur musicale ou un lecteur de vidéo Youtube.</p>

